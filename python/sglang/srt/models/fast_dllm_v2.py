"""SGLang-native Fast_dLLM_v2 model.

Fast_dLLM_v2 checkpoints declare the Hugging Face architecture
``Fast_dLLM_QwenForCausalLM`` and use a Qwen-style decoder with bidirectional
block attention during diffusion refinement. This module registers a native
SGLang implementation so the model can be loaded by SGLang's model registry and
used as the foundation for the native Fast_dLLM_v2 proposal runtime.
"""

